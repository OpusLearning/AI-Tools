const contentDiv = document.getElementById("content");

// Function to render tools by category
function renderToolsByCategory(toolsByCategory) {
  contentDiv.innerHTML = ""; // Clear existing content
  for (const category in toolsByCategory) {
    const section = document.createElement("section");
    section.className = "tool-category";

    const header = document.createElement("h2");
    header.textContent = category;
    section.appendChild(header);

    toolsByCategory[category].forEach((tool) => {
      const card = document.createElement("div");
      card.className = "tool-card";
      card.innerHTML = `
                 
                  <div class="tool-card-content">
                      <h3>${tool.name}</h3>
                      <p>${tool.description}</p>
                      <a href="${tool.link}" target="_blank">Visit Site</a>
                  </div>
              `;
      section.appendChild(card);
    });

    contentDiv.appendChild(section);
  }
}

// Initial render
renderToolsByCategory(aiTools);

// Function to flatten the tools object into a single array for searching
function flattenTools(toolsByCategory) {
  return Object.values(toolsByCategory).flat();
}

// Search Functionality
document.getElementById("search-bar").addEventListener("input", (e) => {
  const searchTerm = e.target.value.toLowerCase();
  const allTools = flattenTools(aiTools); // Flatten the tools object
  const filteredTools = allTools.filter(
    (tool) =>
      tool.name.toLowerCase().includes(searchTerm) ||
      tool.description.toLowerCase().includes(searchTerm)
  );
  renderToolsByCategory({ "Search Results": filteredTools });
});
